void main(List<String> args) {
  // Dart hanya memiliki 2 tipe data yaitu int (bilangan bulat) dan double (bilangan desimal)

  int number1 = 23;
  print(number1);
  
  double number2 = 35.5;
  print(number2);

  // Jika ingin fleksible mengubah tipe data int ke double atau sebaliknya gunakan num

  num number3 = 48;
  print(number3);

  number3 = 74.6;
  print(number3);

}